using System;
using System.Threading.Tasks;

namespace Unity.Services.Apis.Sample
{
    class PlayerNamesSectionView : SectionView
    {
        public override Section Section => Section.PlayerNames;
        public override string Url => "https://services.docs.unity.com/player-names/v1/";
        public override string Info => "View player names.";

        PlayerNamesController PlayerNames => App.PlayerNames;

        UIElement PlayersContainer;

        public PlayerNamesSectionView(App app) : base(app)
        {
            PlayerNames.OnPlayersChanged += RefreshPlayerGUI;
        }

        protected override void CreateGUISection()
        {
            UI.Snippet().SetText(Snippets.PlayerNamesTrusted).SetPaddingBottom(10);

            using (UI.Element().BindVisibility(App.ValidConfigurationBinding, true).Scope())
            {
                using (UI.Block().SetName("PlayerIdContainer").SetMargin(5, 0, 5, 0).Scope())
                {
                    UI.Label("Player Id").SetWidth(100);
                    UI.TextField().BindValue(UI.BindProperty(() => PlayerNames.CurrentPlayerId)).SetWidth(250);
                    UI.Button("Get", GetPlayerNameAsync).SetWidth(75);
                }

                PlayersContainer = UI.Element();
            }
        }

        private void RefreshPlayerGUI()
        {
            PlayersContainer.Clear();

            using (PlayersContainer.Scope())
            {
                foreach (var player in PlayerNames.Players.Values)
                {
                    using (UI.HeaderPanel().Scope())
                    {
                        UI.H5(player.Id);
                        UI.Flex();
                        UI.Button("X", () => PlayerNames.RemovePlayer(player)).SetWidth(50);
                    }

                    using (UI.ContentPanel().SetMarginBottom(5).Scope())
                    {
                        using (UI.Block().Scope())
                        {
                            UI.Label("Name").SetWidth(150);
                            UI.SelectableLabel().SetText(player.Name);
                        }

                        using (UI.Block().Scope())
                        {
                            UI.Label("Generated").SetWidth(150);
                            UI.SelectableLabel().SetText(player.AutoGenerated.ToString());
                        }
                    }
                }
            }
        }


        async Task GetPlayerNameAsync()
        {
            try
            {
                SetStatus(Status.Warning, $"Retrieving Player Name...");
                await PlayerNames.FetchPlayerNameAsync();
                SetStatus(Status.Success, $"GetPlayerName Successful.");
            }
            catch (Exception e)
            {
                AppLogger.LogError(e.Message);
                SetStatus(Status.Error, "GetPlayerName Failed.", e);
            }
        }
    }
}
